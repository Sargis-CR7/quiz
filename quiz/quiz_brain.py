from data import question_data
class QuizBrain:
    def __init__(self,question_list):
        self.question_number=0
        self.score=0
        self.question_list=question_list
        
    def still_has_question(self):
        return self.question_number<len(self.question_list)
    
    def next_question(self):
        current_question=self.question_list[self.question_number]
        self.question_number +=1
        uzer=input(f"Q{self.question_number}:{current_question.text} (True/False)")
        self.chek_answer(uzer,current_question.answer)
        
    def chek_answer(self,uzer_answer,correct_answer):
        if uzer_answer.lower()==correct_answer.lower():
            self.score+=1
        else:
            print("not correct")
